"""
x and y: False
x or y: True
not x: False
x is y: False
x is not y: True
"""
x = True
y = False

print('x and y :',x and y)
# Output: False

print('x or y :',x or y)
# Output: True

print('not x :', not x)
# Output: False

print('x is y:', x is y)
# Output: False

print('x is not y:', x is not y)
# Output: True